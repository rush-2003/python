# Whenever we see a sorted array —— We implement Binary Search
# But Binary Search can be applied in Unsorted array also
# This is called Binary Search on Answer
# In binary search we initilize start, end and mid
# We compare mid with key, start, end then take decision —— This is Condition
# In case of Binary Search on Answer we need to derive this condition —— Not that easy
# After deriving that condition we can further decide and apply Binary Search on Unsorted Array